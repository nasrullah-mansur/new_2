        <script>

            // Table filtering;
            const getCellValue = (tr, idx) => tr.children[idx].innerText || tr.children[idx].textContent;
            const comparer = (idx, asc) => (a, b) => ((v1, v2) => (v1 !== "" && v2 !== "" && !isNaN(v1) && !isNaN(v2) ? v1 - v2 : v1.toString().localeCompare(v2)))(getCellValue(asc ? a : b, idx), getCellValue(asc ? b : a, idx));
            document.querySelectorAll("th").forEach((th) =>
                th.addEventListener("click", () => {
                    const table = th.closest("table");
                    Array.from(table.querySelectorAll("tr:nth-child(n+2)"))
                        .sort(comparer(Array.from(th.parentNode.children).indexOf(th), (this.asc = !this.asc)))
                        .forEach((tr) => table.appendChild(tr));
                })
            );
        
            jQuery(".coll-btn a").on("click", function (e) {
                e.preventDefault();
                jQuery(".col-content").toggle();
                jQuery(".coll-btn").toggleClass("active");
            });
            jQuery(".table .tb-head img").on("click", function () {
                jQuery(this).parent().find(".notice-text").toggle();
            });

            jQuery(".table th").on("click", function () {
                let className = jQuery(this).attr("class");
                var tdIndexCount = 0;
                if (className === "one") {
                    jQuery(".table .tr td:not(.one)").removeClass("active");
                    tdIndexCount = 1;
                } else if (className === "two") {
                    jQuery(".table .tr td:not(.two)").removeClass("active");
                    tdIndexCount = 2;
                } else if (className === "three") {
                    jQuery(".table .tr td:not(.three)").removeClass("active");
                    tdIndexCount = 3;
                } else if (className === "four") {
                    jQuery(".table .tr td:not(.four)").removeClass("active");
                    tdIndexCount = 4;
                }

                jQuery(".table .tr td:nth-child(" + tdIndexCount + ")").addClass("active");
            });

            jQuery(".table .th th").on("click", function () {
                jQuery(".table .th th").removeClass("border-color");
                jQuery(this).addClass("border-color");
                jQuery(this).addClass("border-color");
            });
        </script>
